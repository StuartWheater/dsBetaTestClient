context("ds.mean.o()::smoke::type=combine")
test_that("mean values [combine]", {
    ds.rm.o("mean.all.studies")
    ds.rm.o("mean.study.specific")
    ds.rm.o("Nvalid.all.studies")
    ds.rm.o("Nvalid.study.specific")

    stat.mean <- ds.mean.o(x='D$LAB_TSC',type='combine')

    expect_length(stat.mean, 3)
    expect_length(stat.mean$Global.Mean, 4)
    expect_equal(as.numeric(stat.mean$Global.Mean[1]), 5.85192485623003, tolerance = .000000000000001)
    expect_equal(as.integer(stat.mean$Global.Mean[2]), 1554)
    expect_equal(as.integer(stat.mean$Global.Mean[3]), 7825)
    expect_equal(as.integer(stat.mean$Global.Mean[4]), 9379)
    expect_equal(stat.mean$Nstudies, 3)
    expect_length(stat.mean$ValidityMessage, 3)
    expect_equal(stat.mean$ValidityMessage[1], "VALID ANALYSIS")
    expect_equal(stat.mean$ValidityMessage[2], "VALID ANALYSIS")
    expect_equal(stat.mean$ValidityMessage[3], "VALID ANALYSIS")

    ls.res <- ds.ls(datasources=ds.test_env$connection.opal)
    expect_false("mean.all.studies" %in% ls.res$sim1)
    expect_false("mean.study.specific" %in% ls.res$sim1)
    expect_false("Nvalid.all.studies" %in% ls.res$sim1)
    expect_false("Nvalid.study.specific" %in% ls.res$sim1)
    expect_false("mean.all.studies" %in% ls.res$sim2)
    expect_false("mean.study.specific" %in% ls.res$sim2)
    expect_false("Nvalid.all.studies" %in% ls.res$sim2)
    expect_false("Nvalid.study.specific" %in% ls.res$sim2)
    expect_false("mean.all.studies" %in% ls.res$sim3)
    expect_false("mean.study.specific" %in% ls.res$sim3)
    expect_false("Nvalid.all.studies" %in% ls.res$sim3)
    expect_false("Nvalid.study.specific" %in% ls.res$sim3)
})

